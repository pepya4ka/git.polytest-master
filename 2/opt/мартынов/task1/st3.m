function [c, ceq] = constrStoh(x)
ceq = 0;
 
sigma = probabilityConstr(x);
K=1.282;
c = [
    % введенное вероятностное ограничение
    0.6*x(1) + 0.8*x(2) + 1 * x(3) + 1.2*x(4) - 100 + K*sigma;
 
    -0.2*x(1) - 0.3*x(2) - 0.4*x(3) - 0.5*x(4) + 35;
    -0.6*x(1) - 0.4*x(2) - 0.3*x(3) - 0.2*x(4) + 42;
    % условия из "бывших" критериев
     -12*x(1) - 5*x(2) - 15*x(3) - 10*x(4)];
end

